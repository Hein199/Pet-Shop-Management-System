    // Funtion to show data on the sale record table
    private void tableLoad(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/PetShopManagementSysDB","root","");
            DefaultTableModel dt = (DefaultTableModel)saleRecTb.getModel();
            PreparedStatement pst = con.prepareStatement("SELECT * from SaleRecord");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                Object o[] = {rs.getString("Sale_id"), rs.getString("Category"), rs.getString("Breed"),rs.getString("Qty"), rs.getString("Price"),rs.getString("Total")};
                dt.addRow(o);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void btnTableLoad(String category){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/PetShopManagementSysDB","root","");
            DefaultTableModel dt = (DefaultTableModel)saleRecTb.getModel();
            dt.setRowCount(0);
            PreparedStatement pst = con.prepareStatement("SELECT * from SaleRecord WHERE Category = ?");
            pst.setString(1, category);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                Object o[] = {rs.getString("Sale_id"), rs.getString("Category"), rs.getString("Breed"),rs.getString("Qty"), rs.getString("Price"),rs.getString("Total")};
                dt.addRow(o);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
