    private void txtfeIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfeIdKeyReleased
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/PetShopManagementSysDB","root","");
            DefaultTableModel dt = (DefaultTableModel)saleRecTb.getModel();
            dt.setRowCount(0);
            
            String id = txtfeId.getText();
            PreparedStatement pst = con.prepareStatement("SELECT * FROM SaleRecord WHERE Sale_id = ?");
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();

            while(rs.next()){
                Object o[] = {rs.getString("Sale_id"), rs.getString("Category"), rs.getString("Breed"),rs.getString("Qty"), rs.getString("Price"),rs.getString("Total")};
                dt.addRow(o);
                int total = 0;
                for (int i=0; i<saleRecTb.getRowCount(); i++){
                    total += Integer.parseInt(saleRecTb.getValueAt(i, 5).toString());
                    txtfeTotal.setText(Integer.toString(total));
                }
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txtfeIdKeyReleased

