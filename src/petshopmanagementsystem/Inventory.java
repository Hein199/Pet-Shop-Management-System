    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String breed, quantity, price, id;
        breed = txtfeBreed.getText();
        quantity = txtfeQty.getText();
        price = txtfePrice.getText();
        id = txtfeId.getText();

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/PetShopManagementSysDB","root","");
            
            String table = "";
            if (rbtndog.isSelected()) {
            table = "Dog";
            } else if (rbtncat.isSelected()) {
            table = "Cat";
            } else if (rbtnrabbit.isSelected()) {
            table = "Rabbit";
            } else if (rbtnbird.isSelected()) {
            table = "Bird";
            }
            
            String sql = "UPDATE " + table + " SET Breed=?, Quantity=?, Price=? WHERE " + table + "_id=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, breed);
            pst.setString(2, quantity);
            pst.setString(3, price);
            pst.setString(4, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Inventory Updated Successfully!");
            txtfeBreed.setText("");
            txtfeQty.setText("");
            txtfePrice.setText("");
            txtfeId.setText("");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

