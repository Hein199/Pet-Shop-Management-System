    private void txtfeIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfeIdKeyReleased
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/PetShopManagementSysDB","root","");
            String id = txtfeId.getText();
            
            String table = "";
            if (rbtndog.isSelected()) {
            table = "Dog";
            } else if (rbtncat.isSelected()) {
            table = "Cat";
            } else if (rbtnrabbit.isSelected()) {
            table = "Rabbit";
            } else if (rbtnbird.isSelected()) {
            table = "Bird";
            }
            
            DefaultTableModel dt = (DefaultTableModel)tableInventory.getModel();
            dt.setRowCount(0);
            
            if (!id.isEmpty()) { // Check if the ID field is not empty
                PreparedStatement pst = con.prepareStatement("SELECT " + table + "_id, Breed, Price, Quantity FROM " + table + " WHERE " + table + "_id = ?");
                pst.setString(1, id);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    String breed = rs.getString(2);
                    String price = rs.getString(3);
                    String qty = rs.getString(4);
                    txtfeBreed.setText(breed);
                    txtfePrice.setText(price);
                    txtfeQty.setText(qty);
                } else {
                    txtfeBreed.setText("");
                    txtfePrice.setText("");
                    txtfeQty.setText("");
                }

                do {
                    Object o[] = {rs.getString(table + "_id"), rs.getString("Breed"), rs.getString("Quantity"), rs.getString("Price")};
                    dt.addRow(o);
                } while (rs.next());
            } else {
                txtfeBreed.setText("");
                txtfePrice.setText("");
                txtfeQty.setText("");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txtfeIdKeyReleased

